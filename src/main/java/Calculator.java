import java.util.Scanner;

public class Calculator {
    public double plus(int x, int y) {
        return x + y;
    }

    public double minus(int x, int y) {
        return x - y;
    }

    public double mul(int x, int y) {
        return x * y;
    }

    public double div(int x, int y) {
        return x / y;
    }
}
